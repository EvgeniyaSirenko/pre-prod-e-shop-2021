package com.epam.preprod.sirenko.strategy.impl;

import com.epam.preprod.sirenko.entity.Food;
import com.epam.preprod.sirenko.strategy.ProductCreationStrategy;
import org.junit.jupiter.api.Test;

import java.math.BigDecimal;

import static org.mockito.Mockito.mock;
import static org.junit.jupiter.api.Assertions.*;
import static org.mockito.Mockito.when;

class FoodFactoryImplTest {
	
	@Test
	void createProductShouldCreateNewProduct() {
		ProductCreationStrategy productCreationStrategy = mock(AutoGeneratedProductCreationStrategy.class);
		when(productCreationStrategy.getBigDecimal()).thenReturn(BigDecimal.ONE);
		when(productCreationStrategy.getStringName()).thenReturn("Name");
		when(productCreationStrategy.getInt()).thenReturn(10);
		FoodFactoryImpl foodFactory = new FoodFactoryImpl(productCreationStrategy);
		
		Food product = (Food) foodFactory.createProduct();
		
		assertNotNull(product);
		assertEquals(BigDecimal.valueOf(1), product.getPrice());
		assertEquals("Name", product.getName());
		assertEquals(10, product.getWeight());
	}
}